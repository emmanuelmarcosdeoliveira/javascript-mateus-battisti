// Operadores ternários
// são comparados os valores lógicos onde se a a primeira expressão é verdaeira ele retorna o valor depois do Simbolo ( ? )  se for falso ele retorna o valor depois desse simbolo( : )

// Essa comparação nos retorna um valor boolean ( veradeiro o u falso)

// Exemplo:

console.log(5 > 3 ? "É sim" : "É não");
console.log(5 > 8 ? "É sim" : "É não");
console.log("Emmanuel" == "Emmanuel" ? "Olá Emmanuel" : "Não é o Emmanuel")
