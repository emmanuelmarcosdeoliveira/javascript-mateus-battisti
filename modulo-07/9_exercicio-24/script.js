function potencia (a,b) {
  return Math.pow(a,b) // Math.pow é uma funcção da biclioteca Math. aonde a é o numero base e o b (a potencia)
}
console.log(potencia(2,2));
console.log(potencia(3,2));
console.log(potencia(4,4));

